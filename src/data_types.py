# Floats
x = 0.1
print("x={0} est de type {1}".format(x,type(x)))

x = 1.0
print("x={0} est de type {1}".format(x,type(x)))

# Integers
x = 1
print("x={0} est de type {1}".format(x,type(x)))

# Booleans
print("L'expression 3==4 vaut {0} et est de type {1}".format(3==4,type(3==4)))
print("L'expression 3<=4 vaut {0} et est de type {1}".format(3<=4,type(3<=4)))

# etc, there are several important data structures in python,
